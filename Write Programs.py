# Write a program to calculate volume of sphere

# Formula of volume calculation: (4/3) * 3.145 * (radius ** 3)

radius = float(input("Enter radius of sphere: "))
volume = (4/3) * 3.145 * (radius ** 3)

print("Volume of sphere is: ", volume)


# Write a program to convert temperature from celcius to fahrenheit

temp_in_celcius = float(input("Enter temperature in celcius: "))
fahrenheit = (temp_in_celcius * 9 / 5) + 32

print ("Temperature in Fahrenheit: ", fahrenheit)